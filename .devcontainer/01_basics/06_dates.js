let mydate = new Date();
console.log(mydate.toString())  //Wed Jun 05 2024 12:00:00 GMT+0000 (Coordinated Universal Time);
console.log(mydate.toDateString())  //Wed Jun 05 2024
console.log(mydate.toISOString())  //2024-06-05T12:00:00.000Z
console.log(mydate.toLocaleDateString())  //6/5/2024
